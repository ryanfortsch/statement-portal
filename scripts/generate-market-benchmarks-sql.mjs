#!/usr/bin/env node
/**
 * Generates `supabase/migrations/20260506d_seed_market_benchmarks.sql`
 * from the three AirDNA CSVs in `data/airdna/`.
 *
 * Run from repo root:
 *   node scripts/generate-market-benchmarks-sql.mjs
 *
 * Re-run if AirDNA gives us newer monthly data — the migration uses
 * ON CONFLICT DO NOTHING so re-applying is safe and idempotent.
 *
 * The CSV format (verified 2026-05-06) is:
 *   "Date","1 bedroom","2 bedroom","3 bedroom","4 bedroom","5 bedroom","6+ bedroom"
 *   "2018-01-01","833.2","1625.2","454.14","1347.5","1924.67","542"
 *   ...
 *
 * Empty cells (no AirDNA observation that month for that cohort) are
 * skipped — we'd rather have a missing comp than a fake zero.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = resolve(__dirname, '..');

const MARKETS = [
  { key: 'gloucester', file: 'gloucester.csv' },
  { key: 'rockport', file: 'rockport.csv' },
  { key: 'beverly', file: 'beverly.csv' },
];

// Order matches CSV column order after the Date column.
const BEDROOM_COLUMNS = [1, 2, 3, 4, 5, 6];

/**
 * Lightweight CSV parser tuned to AirDNA exports. Each line is
 * "YYYY-MM-DD","val","val","val","val","val","val" — the values are
 * already quoted strings, no embedded commas.
 */
function parseCsv(text) {
  const lines = text.trim().split(/\r?\n/);
  // skip header
  return lines.slice(1).map((line) => {
    const cells = line.split(',').map((c) => c.replace(/^"|"$/g, ''));
    const date = cells[0];
    const [yearStr, monthStr] = date.split('-');
    return {
      year: Number(yearStr),
      month: Number(monthStr),
      values: cells.slice(1).map((v) => (v === '' ? null : Number(v))),
    };
  });
}

const rows = [];
for (const { key, file } of MARKETS) {
  const path = resolve(repoRoot, 'data/airdna', file);
  const csv = readFileSync(path, 'utf8');
  const parsed = parseCsv(csv);
  for (const { year, month, values } of parsed) {
    values.forEach((v, idx) => {
      if (v == null || Number.isNaN(v)) return;
      rows.push({
        market: key,
        bedrooms: BEDROOM_COLUMNS[idx],
        year,
        month,
        avg_revenue: v,
      });
    });
  }
}

const valuesSql = rows
  .map((r) =>
    `  ('${r.market}', ${r.bedrooms}, ${r.year}, ${r.month}, ${r.avg_revenue.toFixed(2)})`,
  )
  .join(',\n');

const out = `-- AirDNA monthly revenue benchmarks for Gloucester / Rockport / Beverly,
-- 1 through 6+ bedrooms, January 2018 through April 2026.
--
-- This file is generated by scripts/generate-market-benchmarks-sql.mjs from
-- the CSVs in data/airdna/. Do not edit by hand. Re-run the script instead.
--
-- Total rows: ${rows.length}
-- Source: AirDNA monthly revenueByBedroom export, pulled 2026-05-06.

insert into public.market_revenue_benchmarks
  (market, bedrooms, year, month, avg_revenue)
values
${valuesSql}
on conflict (market, bedrooms, year, month, source) do nothing;
`;

const outPath = resolve(repoRoot, 'supabase/migrations/20260506d_seed_market_benchmarks.sql');
writeFileSync(outPath, out);
console.log(`Wrote ${rows.length} rows to ${outPath}`);
